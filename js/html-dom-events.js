/*
 * HTML DOM EVENTS
 * List of DOM events (not all).
 */

/**
 * Animation events:
 */
// animationstart - event occurs when a CSS animation has started to play
// animationend - occurs when the CSS animation has completed
// animationiteration - occurs when the CSS animation is repeated

/**
 * Events fired on the draggable target (the source element):
 * Note: Links and images are draggable by default, and do not need the draggable attribute.
 *       To make an element draggable, use the global HTML5 draggable attribute.
 */
// dragstart - occurs when the user starts to drag an element
// drag - occurs when an element is being dragged
// dragend - occurs when the user has finished dragging the element

/**
 * Events fired on the drop related events:
 */
// dragenter - occurs when the dragged element enters the drop target
// dragover - occurs when the dragged element is over the drop target
// dragleave - occurs when the dragged element leaves the drop target
// drop - occurs when the dragged element is dropped on the drop target

/**
 * Key events:
 */
// keydown - event occurs when the user is pressing a key (on the keyboard)
// keypress - event occurs when the user presses a key (on the keyboard)
// keyup - event occurs when the user releases a key (on the keyboard)

/**
 * Mouse events:
 */
// mousedown - event occurs when a user presses a mouse button over an element
// mouseup - event occurs when a user releases a mouse button over an element
// click - event occurs when the user clicks on an element
// dblclick - event occurs when the user double-clicks on an element
// mouseenter - event occurs when the mouse pointer is moved onto an element
// mouseleave - event occurs when the mouse pointer is moved out of an element
// mousemove -  event occurs when the pointer is moving while it is over an element
// mouseover - event occurs when the mouse pointer is moved onto an element, or onto one of its children
// mouseout - event occurs when the mouse pointer is moved out of an element, or out of one of its children

/**
 * Touch screen events:
 */
// touchstart - occurs when the user touches an element
// touchend - event occurs when the user removes the finger from an element
// touchmove - occurs when the user moves the finger across the screen
// touchcancel - occurs when the touch is interrupted
// wheel - event occurs when the mouse wheel is rolled up or down over an element, event also occurs when the user scrolls or zooms in or out of an element by using a touchpad

/**
 * Other:
 */
// blur - event occurs when an object loses focus
// change - event occurs when the content of a form element, the selection, or the checked state have changed (for <input>, <select>, and <textarea>)
// focus - event occurs when an element gets focus, event is most often used with <input>, <select>, and <a>
// input - event occurs when an element gets user input (when <input> or <textarea> element is changed)
// load - event occurs when an object has been loaded
// reset - event occurs when a form is reset
// resize - event occurs when the browser window has been resized
// scroll - event occurs when an element's scrollbar is being scrolled
// search - event occurs when a user presses the 'ENTER' key or clicks the 'x' button in an <input> element with type="search"
// select - event occurs after some text has been selected in an element
// submit - event occurs when a form is submitted
