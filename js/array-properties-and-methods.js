/*
 * ARRAY PROPERTIES AND METHODS
 * List of array properties and methods.
 */

 // sets or returns the number of elements in an array
 // array.length
 // length

 // joins two or more arrays
 // array1.concat(array2, array3, ..., arrayX)
 // concat();

 // copies array elements to another position in the array, overwriting the existing values
 // array.copyWithin(target, start, end)
 // copyWithin();

 // returns an Array Iterator object with key/value pairs
 // array.entries()
 // entries();

 // checks if all elements in an array pass a test (provided as a function), executes the function once for each element present in the array
 // array.every(function(currentValue, index, arr), thisValue)
 // every();

 // fills the specified elements in an array with a static value
 // array.fill(value, start, end)
 // fill();

 // creates an array filled with all array elements that pass a test (provided as a function)
 // array.filter(function(currentValue, index, arr), thisValue)
 // filter();

 // returns the value of the first element in an array that pass a test (provided as a function), executes the function once for each element present in the array
 // array.find(function(currentValue, index, arr),thisValue)
 // find();

 // returns the index of the first element in an array that pass a test (provided as a function), executes the function once for each element present in the array
 // array.findIndex(function(currentValue, index, arr), thisValue)
 // findIndex();

 // calls a function once for each element in an array, in order
 // array.forEach(function(currentValue, index, arr), thisValue)
 // forEach();

 // returns an Array object from any object with a length property or an iterable object
 // Array.from(object, mapFunction, thisValue)
 // from();

 // determines whether an array contains a specified element
 // array.includes(element, start)
 // includes();

 // searches the array for the specified item, and returns its position, returns -1 if the item is not found
 // array.indexOf(item, start)
 // indexOf();

 // determines whether an object is an array
 // Array.isArray(obj)
 // isArray();

 // returns the array as a string
 // array.join(separator)
 // join();

 // returns an Array Iterator object with the keys of an array
 // array.keys()
 // keys();

 // searches the array for the specified item, and returns its position, returns -1 if the item is not found (if the item is present more than once, returns last position)
 // array.lastIndexOf(item, start)
 // lastIndexOf();

 // creates a new array with the results of calling a function for every array element, calls the provided function once for each element in an array, in order
 // array.map(function(currentValue, index, arr), thisValue)
 // map();

 // removes the last element of an array, and returns that element
 // array.pop()
 // pop();

 // adds new items to the end of an array, and returns the new length
 // array.push(item1, item2, ..., itemX)
 // push();

 // reduces the array to a single value, executes a provided function for each value of the array (from left-to-right)
 // array.reduce(function(total, currentValue, currentIndex, arr), initialValue)
 // reduce();

 // reduces the array to a single value, executes a provided function for each value of the array (from right-to-left)
 // array.reduceRight(function(total, currentValue, currentIndex, arr), initialValue)
 // reduceRight();

 // reverses the order of the elements in an array
 // array.reverse()
 // reverse();

 // removes the first item of an array
 // array.shift()
 // shift();

 // returns the selected elements in an array, as a new array object, selects the elements starting at the given start argument, and ends at, but does not include, the given end argument
 // array.slice(start, end)
 // slice();

 // checks if any of the elements in an array pass a test (provided as a function), executes the function once for each element present in the array
 // array.some(function(currentValue, index, arr), thisValue)
 // some();

 // sorts the items of an array, order can be either alphabetic or numeric, and either ascending (up) or descending (down)
 // array.sort(compareFunction)
 // sort();

 // adds/removes items to/from an array, and returns the removed item(s)
 // array.splice(index, howmany, item1, ....., itemX)
 // splice();

 // returns a string with all the array values, separated by commas
 // array.toString()
 // toString();

 // adds new items to the beginning of an array, and returns the new length
 // array.unshift(item1, item2, ..., itemX)
 // unshift();
